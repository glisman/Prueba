
public class Circulo {
	
	private int radio;
	private int punto;
	
	
	public Circulo(int radio, int punto) {
		this.setRadio(radio);
		this.setPunto(punto);		
	}


	public int getRadio() {
		return radio;
	}


	public void setRadio(int radio) {
		this.radio = radio;
	}


	public int getPunto() {
		return punto;
	}


	public void setPunto(int punto) {
		this.punto = punto;
	}
}

